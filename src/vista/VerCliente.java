    private void filtrarTablaPorRUT() {
        String texto = BuscarPorRutjTextField.getText().trim();
        
        if (texto.length() == 0) {
            sorter.setRowFilter(null); // Mostrar todos
        } else {
            try {
                // Filtramos por la columna 0 (RUT). 
                // Buscamos coincidencias que *contengan* el texto ingresado.
                sorter.setRowFilter(RowFilter.regexFilter("(?i)" + texto, 0)); 
            } catch (java.util.regex.PatternSyntaxException e) {
                sorter.setRowFilter(null); 
                JOptionPane.showMessageDialog(this, "RUT ingresado inválido.", "Error de Búsqueda", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
