    private void inicializarTabla() {
        // 1. Definir las columnas
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"RUT", "Nombre", "Direccion", "Tipo"}, 0
        );
        ClienteTabla.setModel(model);

        // 2. Obtener datos (Asumiendo que el método existe en el controlador)
        List<Distribuidor> lista = controlVentas.obtenerClientes(); 
        
        // 3. Llenar filas
        for (Distribuidor d : lista) {
            model.addRow(new Object[]{
                d.getRut(),
                d.getNombre(),
                d.getDireccion(),
                d.getTipo() 
            });
        }
        
        // 4. Inicializar el sorter para habilitar el filtrado
        sorter = new TableRowSorter<>(model);
        ClienteTabla.setRowSorter(sorter);
    }
    
